export const generateCombinatorialSurge = (): MagicSurge => {
  // Мы создаем матрицу для дополнительных сотен вариаций,
  // комбинируя Цель, Эффект и Механику.
  
  const targets = [
    "Вы",
    "Случайное существо в 30 футах",
    "Ближайший враг",
    "Вы и все существа в 15 футах",
    "Ваше оружие или фокус",
    "Ближайший союзник"
  ];
  
  const events = [
    { name: "Окаменение", flavor: "Кожа покрывается серой коркой.", mechanic: "Цель получает эффект заклинания 'Замедление' (Slow)." },
    { name: "Левитация", flavor: "Ноги отрываются от земли.", mechanic: "Цель начинает левитировать на высоте 5 футов от земли." },
    { name: "Стихийный Всплеск", flavor: "Происходит выброс неконтролируемой энергии.", mechanic: "Цель получает 1d10 урона случайным типом урона (огонь, холод, электричество или кислота)." },
    { name: "Ускорение", flavor: "Движения становятся размытыми от скорости.", mechanic: "Цель получает эффекты заклинания 'Ускорение' (Haste)." },
    { name: "Забвение", flavor: "Мысли стираются из памяти на пару секунд.", mechanic: "Цель забывает всё, что происходило последние 6 секунд. Бросок Мудрости или ступор на 1 ход." },
    { name: "Зов Пустоты", flavor: "Звук пропадает, остаются лишь тени.", mechanic: "Цель становится глухой и невидимой." },
    { name: "Неуязвимость", flavor: "Кожа наливается золотым свечением.", mechanic: "Цель получает сопротивление ко всем видам урона." },
    { name: "Проклятие Слабости", flavor: "Мышцы атрофируются на глазах.", mechanic: "Атаки ближнего боя цели наносят урон на половину меньше." }
  ];

  const durations = [
    "1 раунд",
    "1 минута",
    "До конца следующего хода",
    "1d4 раундов",
    "Отменяется при получении урона"
  ];

  const target = targets[Math.floor(Math.random() * targets.length)];
  const event = events[Math.floor(Math.random() * events.length)];
  const duration = durations[Math.floor(Math.random() * durations.length)];
  
  const isBuff = event.mechanic.includes("сопротивление") || event.mechanic.includes("Haste");
  const isDebuff = event.mechanic.includes("Замедление") || event.mechanic.includes("урона") || event.mechanic.includes("меньше") || event.mechanic.includes("глухой");

  let type: 'buff' | 'debuff' | 'neutral' | 'chaotic' = 'chaotic';
  if (isBuff && target === "Вы") type = 'buff';
  if (isDebuff && target === "Вы") type = 'debuff';
  if (isBuff && target.includes("враг")) type = 'debuff';

  return {
    id: `rnd-${Math.random().toString(36).substr(2, 9)}`,
    title: `Хаос: ${event.name}`,
    flavor: event.flavor,
    mechanic: `${target}: ${event.mechanic}`,
    duration: duration,
    type: type,
    timestamp: 0
  };
};

export const getRandomSurge = (): MagicSurge => {
  // 60% chance to pick handcrafted, 40% chance to generate one from the combinatorial matrix
  // resulting in theoretically 6 * 8 * 5 = 240 additional unique combinations,
  // bringing the total well over 200 distinct outcomes.
  
  const isHardcoded = Math.random() > 0.4;
  let surge: MagicSurge;
  
  if (isHardcoded) {
    surge = predefinedSurges[Math.floor(Math.random() * predefinedSurges.length)];
  } else {
    surge = generateCombinatorialSurge();
  }
  
  return {
    ...surge,
    id: `ws-${Date.now()}-${Math.random().toString(36).substring(2, 7)}`,
    timestamp: Date.now()
  };
};
